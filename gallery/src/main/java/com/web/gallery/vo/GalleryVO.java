package com.web.gallery.vo;

public class GalleryVO {
	
	int no;
	String gallery_title;
	String gallery_img;
	String gallery_path;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getGallery_title() {
		return gallery_title;
	}
	public void setGallery_title(String gallery_title) {
		this.gallery_title = gallery_title;
	}
	public String getGallery_img() {
		return gallery_img;
	}
	public void setGallery_img(String gallery_img) {
		this.gallery_img = gallery_img;
	}
	public String getGallery_path() {
		return gallery_path;
	}
	public void setGallery_path(String gallery_path) {
		this.gallery_path = gallery_path;
	}
	
	
	

}
