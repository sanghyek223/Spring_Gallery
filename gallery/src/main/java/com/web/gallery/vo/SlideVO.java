package com.web.gallery.vo;

public class SlideVO {

	int no;
	int slide_check;
	String slide_img;
	String slide_path;
	
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getSlide_check() {
		return slide_check;
	}
	public void setSlide_check(int slide_check) {
		this.slide_check = slide_check;
	}
	public String getSlide_img() {
		return slide_img;
	}
	public void setSlide_img(String slide_img) {
		this.slide_img = slide_img;
	}
	public String getSlide_path() {
		return slide_path;
	}
	public void setSlide_path(String slide_path) {
		this.slide_path = slide_path;
	}
}
